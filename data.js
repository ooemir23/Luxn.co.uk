function getItemAmenities(id, category) {
  const amenByCat = { stay: AMENITIES_STAY, drive: AMENITIES_CAR, sail: AMENITIES_YACHT };
  const all = amenByCat[category] || [];
  const hash = id.split("").reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const subset = all.filter((_, idx) => (hash + idx) % 2 === 0 || (hash + idx) % 3 === 0);
  return subset.length > 0 ? subset : all.slice(0, 3);
}

